%     'interpreter','latex',...
%     'FontSize',12,...
%     'FontName','Times',...
%     'Location','NorthEast')
% title('Sinusoidal function',...
%     'FontUnits','points',...
%     'FontWeight','normal',...
%     'FontSize',7,...
%     'FontName','Times')
print(strcat(ylab,'.png'),'-dpng')
